static void render_char(uint8_t *framebuffer, int fb_width, int fb_height, int depth,
                        int x, int y, char ch, uint8_t alpha)
{
    if (ch < 32 || ch > 126) return;
    int idx = ch - 32;
    const uint8_t *glyph = font5x7[idx];

    int bpp = depth / 8;
    if (bpp < 3) bpp = 3;

    for (int row = 0; row < WATERMARK_FONT_HEIGHT; row++) {
        int py = y + row;
        if (py < 0 || py >= fb_height) continue;

        for (int col = 0; col < WATERMARK_FONT_WIDTH; col++) {
            int px = x + col;
            if (px < 0 || px >= fb_width) continue;

            if (glyph[row] & (1 << (7 - col))) {
                int offset = (py * fb_width + px) * bpp;

                if (bpp == 4) {
                    uint8_t fg_r = 255, fg_g = 255, fg_b = 0;
                    uint8_t bg_r = framebuffer[offset + 2];
                    uint8_t bg_g = framebuffer[offset + 1];
                    uint8_t bg_b = framebuffer[offset];
                    framebuffer[offset + 2] = (fg_r * alpha + bg_r * (255 - alpha)) / 255;
                    framebuffer[offset + 1] = (fg_g * alpha + bg_g * (255 - alpha)) / 255;
                    framebuffer[offset]     = (fg_b * alpha + bg_b * (255 - alpha)) / 255;
                } else if (bpp == 3) {
                    uint8_t fg_r = 255, fg_g = 255, fg_b = 0;
                    uint8_t bg_r = framebuffer[offset];
                    uint8_t bg_g = framebuffer[offset + 1];
                    uint8_t bg_b = framebuffer[offset + 2];
                    framebuffer[offset]     = (fg_r * alpha + bg_r * (255 - alpha)) / 255;
                    framebuffer[offset + 1] = (fg_g * alpha + bg_g * (255 - alpha)) / 255;
                    framebuffer[offset + 2] = (fg_b * alpha + bg_b * (255 - alpha)) / 255;
                }
            }
        }
    }
}

static void render_text(uint8_t *framebuffer, int fb_width, int fb_height, int depth,
                        int x, int y, const char *text, uint8_t alpha)
{
    int cursor_x = x;
    for (int i = 0; text[i] != '\0'; i++) {
        if (text[i] == '\n') {
            cursor_x = x;
            y += WATERMARK_FONT_HEIGHT + 2;
            continue;
        }
        render_char(framebuffer, fb_width, fb_height, depth, cursor_x, y, text[i], alpha);
        cursor_x += WATERMARK_FONT_WIDTH + 1;
    }
}

static int random_range(int min, int max)
{
    if (max <= min) return min;
    return min + rand() % (max - min);
}

void watermark_init(WatermarkState *wm, int screen_width, int screen_height)
{
    memset(wm, 0, sizeof(*wm));
    wm->screen_width = screen_width;
    wm->screen_height = screen_height;
    wm->initialized = true;
    wm->last_move_time = (int)time(NULL);
    wm->x = 50;
    wm->y = 50;
    srand((unsigned int)time(NULL));
}

void watermark_update_text(WatermarkState *wm, const char *client_ip, const char *username)
{
    time_t now = time(NULL);
    struct tm *tm_info = localtime(&now);
    char time_str[64];
    strftime(time_str, sizeof(time_str), "%Y-%m-%d %H:%M:%S", tm_info);

    snprintf(wm->text, sizeof(wm->text),
             "%s | IP:%s | User:%s",
             time_str,
             client_ip ? client_ip : "unknown",
             username ? username : "unknown");
}

void watermark_render(WatermarkState *wm, uint8_t *framebuffer, int width, int height, int depth)
{
    if (!wm->initialized || !framebuffer) return;
    render_text(framebuffer, width, height, depth, wm->x, wm->y, wm->text, WATERMARK_ALPHA);
}

void watermark_maybe_move(WatermarkState *wm)
{
    if (!wm->initialized) return;

    int now = (int)time(NULL);
    if (now - wm->last_move_time < WATERMARK_MOVE_INTERVAL) return;

    int text_len = (int)strlen(wm->text);
    int text_width = text_len * (WATERMARK_FONT_WIDTH + 1);
    int text_height = WATERMARK_FONT_HEIGHT;

    int max_x = wm->screen_width - text_width - 20;
    int max_y = wm->screen_height - text_height - 20;

    if (max_x < 20) max_x = 20;
    if (max_y < 20) max_y = 20;

    wm->x = random_range(10, max_x);
    wm->y = random_range(10, max_y);
    wm->last_move_time = now;
}
